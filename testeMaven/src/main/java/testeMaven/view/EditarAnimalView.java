    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        String alteracoes = "Deseja realmente SALVAR as alterações?";
        alteracoes += "\n\nNome: " + txtNome.getText();
        alteracoes += "\nRaça: " + txtRaca.getText();
        alteracoes += "\nNome do Dono: " + txtNomeDono.getText();
        alteracoes += "\nCPF: " + cmbDono.getSelectedItem();

        int resultado = JOptionPane.showConfirmDialog(null, alteracoes, "Salvar?", 0);
        if(resultado == 0){
            try {
                Tbanimal animal = new Tbanimal();
                animal.setId(idClasse);
                animal.setNome(txtNome.getText());
                animal.setRaca(txtRaca.getText());
                animal.setTbcliente(clienteSelecionado);
                new AnimaisController().salvar(animal);

                JOptionPane.showMessageDialog(null, "Dados alterados com sucesso!");
                this.dispose();
            }
            catch (Error e) {
                JOptionPane.showMessageDialog(null, "Houve um erro ao tentar salvar as alterações!\nPor favor, tente novamente!");
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

