    private void cmbDonoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbDonoItemStateChanged
        // TODO add your handling code here:
        for(Tbcliente cliente : clientes ){
            if(evt.getItem().toString() == cliente.getCpf()){
                clienteSelecionado = cliente;
                txtNomeDono.setText(cliente.getNome());
            }
        }
    }//GEN-LAST:event_cmbDonoItemStateChanged

