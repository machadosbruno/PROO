    private DefaultTableModel modeloTabela;
    private DefaultTableModel atualizarTabela(){
        IngredientesController ingredientesController = new IngredientesController();
        List<Ingredientes> listaIngredientes = ingredientesController.listarTodos();
        
        String[] colunas = {"ID", "Nome", "Quantidade em Estoque"};
        modeloTabela = new DefaultTableModel(colunas, 0);
        
        try {
            for (Ingredientes ingrediente : listaIngredientes) {
            Object[] linha = {
                ingrediente.getId(),
                ingrediente.getNome(),
                ingrediente.getQuantidadeEstoque(), 
            };
            modeloTabela.addRow(linha);
            }
            return modeloTabela;
        }
        catch (Error e) {
            JOptionPane.showMessageDialog(null, e);
            return new DefaultTableModel();
        }
    }
    
