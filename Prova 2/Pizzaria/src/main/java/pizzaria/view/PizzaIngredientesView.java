    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tblngredientes.getSelectedRow();
        if(linhaSelecionada != -1){
            int idIngrediente = (int) modeloTabela.getValueAt(linhaSelecionada, 0);
            
            EditarIngredienteView telaEdicao = new EditarIngredienteView(this, true, idIngrediente);
            telaEdicao.setVisible(true);
            tblngredientes.setModel(atualizarTabela());
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione um cadastro para editar!");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tblngredientes.getSelectedRow();
        if(linhaSelecionada != -1){
            int idIngrediente = (int) modeloTabela.getValueAt(linhaSelecionada, 0);
            String nomeIngrediente = modeloTabela.getValueAt(linhaSelecionada, 1).toString();
            int resultado = JOptionPane.showConfirmDialog(null, "Deseja realmente EXCLUIR o ingrediente " + nomeIngrediente + "?", "Excluir?", 0);
            if(resultado == 0){
                new IngredientesController().remover(idIngrediente);
                tblngredientes.setModel(atualizarTabela());
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione um cadastro para excluir!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

