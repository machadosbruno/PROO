    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        String alteracoes = "Deseja realmente SALVAR as alterações?";
        alteracoes += "\n\nNome do Cliente: " + txtNomeCliente.getText();
        alteracoes += "\ncom CPF: " + cmbCpfCliente.getSelectedItem().toString();
        alteracoes += "\n\nNome do Usuário: " + txtNomeUsuario.getText();
        alteracoes += "\ncom CPF: " + cmbCpfUsuario.getSelectedItem().toString();

        int resultado = JOptionPane.showConfirmDialog(null, alteracoes, "Salvar?", 0);
        if(resultado == 0){
            try {
                Pedido pedido = new Pedido();
                pedido.setId(idClasse);
                pedido.setClientes(clienteSelecionado);
                pedido.setUsuarios(usuarioSelecionado);
                new PedidosController().salvar(pedido);

                JOptionPane.showMessageDialog(null, "Dados alterados com sucesso!");
                this.dispose();
            }
            catch (Error e) {
                JOptionPane.showMessageDialog(null, "Houve um erro ao tentar salvar as alterações!\nPor favor, tente novamente!");
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

