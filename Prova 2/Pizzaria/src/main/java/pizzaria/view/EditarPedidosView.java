    private void cmbCpfClienteItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbCpfClienteItemStateChanged
        // TODO add your handling code here:
        for(Clientes cliente : clientes ){
            if(evt.getItem().toString() == cliente.getCpf()){
                clienteSelecionado = cliente;
                txtNomeCliente.setText(cliente.getNome());
            }
        }
    }//GEN-LAST:event_cmbCpfClienteItemStateChanged

    private void cmbCpfUsuarioItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbCpfUsuarioItemStateChanged
        // TODO add your handling code here:
        for(Usuarios usuario : usuarios ){
            if(evt.getItem().toString() == usuario.getCpf()){
                usuarioSelecionado = usuario;
                txtNomeUsuario.setText(usuario.getNome());
            }
        }
    }//GEN-LAST:event_cmbCpfUsuarioItemStateChanged

